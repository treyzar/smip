interface FloatingIcon {
  id: number
  path: string
  x: number
  y: number
  size: number
  opacity: number
  rotation: number
  duration: number
  delay: number
}

export function FloatingSocialIcons() {
  const [icons, setIcons] = useState<FloatingIcon[]>([])

  useEffect(() => {
    const generated: FloatingIcon[] = []
    for (let i = 0; i < 20; i++) {
      generated.push({
        id: i,
        path: socialPaths[i % socialPaths.length],
        x: Math.random() * 100,
        y: Math.random() * 100,
        size: 14 + Math.random() * 20,
        opacity: 0.02 + Math.random() * 0.04,
        rotation: Math.random() * 360,
        duration: 25 + Math.random() * 35,
        delay: Math.random() * -20,
      })
    }
    setIcons(generated)
  }, [])

  return (
    <div className="fixed inset-0 z-0 pointer-events-none overflow-hidden" aria-hidden>
      {icons.map(item => (
        <div
          key={item.id}
          className="absolute animate-float-social"
          style={{
            left: `${item.x}%`,
            top: `${item.y}%`,
            opacity: item.opacity,
            transform: `rotate(${item.rotation}deg)`,
            animationDuration: `${item.duration}s`,
            animationDelay: `${item.delay}s`,
          }}
        >
          <svg width={item.size} height={item.size} viewBox="0 0 24 24" fill="currentColor" className="text-primary/80">
            <path d={item.path} />
          </svg>
        </div>
      ))}
    </div>
  )
}
